attrs = {}   # id -> name
errors = []

for i, cat in enumerate(CATS):
    try:
        r = requests.get(
            f'https://api.mercadolibre.com/categories/{cat}/attributes',
            timeout=10,
        )
        if r.status_code == 200:
            for a in r.json():
                aid   = a.get('id', '')
                aname = a.get('name', '')
                if aid and aid not in attrs:
                    attrs[aid] = aname
        else:
            errors.append(f'{cat}:{r.status_code}')
        if (i + 1) % 50 == 0:
            print(f'  {i+1}/{len(CATS)} procesadas — {len(attrs)} attrs únicos', flush=True)
        time.sleep(0.05)
    except Exception as e:
        errors.append(f'{cat}:{e}')

out = 'ml_attrs_all.tsv'
with open(out, 'w', encoding='utf-8') as f:
    for aid, aname in sorted(attrs.items()):
        f.write(f'{aid}\t{aname}\n')

print(f'\nTotal attrs únicos : {len(attrs)}')
print(f'Errores            : {len(errors)}')
if errors:
    for e in errors[:10]:
        print(f'  {e}')
print(f'Guardado en        : {out}')
